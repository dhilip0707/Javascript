
function validate() {
    let username = document.forms['registration']['user'].value
    let email=document.forms['registration']['email'].value
    let password = document.forms['registration']['pw'].value
    let cpassword = document.forms['registration']['cpw'].value
    


    if (!/^[A-Za-z]{6}$/.test(username)) {
        alert('username must be exactly 6 letters (only A-Z or a-z allowed)');
        return false;
    }
    if(!/^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{3,}$/.test(email)){
        alert('please enter a valid email address ');
        return false;
    }
    if (!/^(?=.*[A-Za-z])(?=.*\d)(?=.*[^A-Za-z\d]).{6}$/.test(password)) {
        alert('password must be exactly 6 characters and include at least one letter, one number, and one special character');
        return false;
    }
    if (password !== cpassword) {
        alert(`password doesn't match`);
        return false;
    }
    alert('Registration Successful');
    return true;
}


























































































































































































































































